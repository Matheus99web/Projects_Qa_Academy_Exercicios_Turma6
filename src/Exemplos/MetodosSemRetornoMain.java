package Exemplos;
//chamando metodo dentro da propria classe.

public class MetodosSemRetornoMain {
    public static void main(String[] args) {
        palmeiras();
        corinthians();
        saopaulo();
        gremio();
        vasco();
    }

    public static void palmeiras() {

        System.out.println("primeiro");
    }

    public static void corinthians() {

        System.out.println("segundo");
    }

    public static void saopaulo() {

        System.out.println("terceiro");
    }

    public static void gremio() {

        System.out.println("quarto");
    }

    public static void vasco() {

        System.out.println("quinto");
    }
}


